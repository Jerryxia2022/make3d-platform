async function createFixtureRoot() {
  return mkdtemp(join(tmpdir(), "make3d-prusaslicer-parser-"));
}

async function writeGcode(root, name, content) {
  await mkdir(root, { recursive: true });
  const filePath = join(root, name);
  await writeFile(filePath, content);
  return filePath;
}

function completeGcode(options = {}) {
  const lineEnding = options.lineEnding || "\n";
  const layers = options.layers ?? 100;
  const stats = options.stats ?? {
    filamentLength: "2116.64",
    filamentVolume: "5.09",
    filamentWeight: "6.31",
    normalTime: "24m 56s",
    silentTime: "25m 44s",
  };
  const config = options.config ?? {
    filamentType: "PLA",
    printerModel: "Bambu Lab P1S",
    nozzleDiameter: "0.4",
    layerHeight: "0.2",
    filamentDensity: "1.24",
    filamentDiameter: "1.75",
  };
  const lines = [
    options.banner ?? "; generated by PrusaSlicer 2.7.2 on 2026-07-14 at 09:25:30 UTC",
    "G90",
  ];

  for (let index = 1; index <= layers; index += 1) {
    lines.push(";LAYER_CHANGE");
    lines.push(`;Z:${(index * 0.2).toFixed(2)}`);
    lines.push(`G1 Z${(index * 0.2).toFixed(2)}`);
    lines.push(`G1 X${index % 2 === 0 ? "20.00" : "0.00"} Y${index % 2 === 0 ? "20.00" : "0.00"}`);
  }

  if (options.longComment) {
    lines.push(`; ${"x".repeat(20 * 1024)}`);
  }

  const statsLines = [
    stats.filamentLength == null ? null : `; filament used [mm] = ${stats.filamentLength}`,
    stats.filamentVolume == null ? null : `; filament used [cm3] = ${stats.filamentVolume}`,
    stats.filamentWeight == null ? null : `; ${stats.filamentWeightKey || "filament used [g]"} = ${stats.filamentWeight}`,
    stats.normalTime == null ? null : `; estimated printing time (normal mode) = ${stats.normalTime}`,
    stats.silentTime == null ? null : `; estimated printing time (silent mode) = ${stats.silentTime}`,
  ].filter(Boolean);
  const configLines = [
    config.filamentType == null ? null : `; filament_type = ${config.filamentType}`,
    config.printerModel == null ? null : `; printer_model = ${config.printerModel}`,
    config.nozzleDiameter == null ? null : `; nozzle_diameter = ${config.nozzleDiameter}`,
    config.layerHeight == null ? null : `; layer_height = ${config.layerHeight}`,
    config.filamentDensity == null ? null : `; filament_density = ${config.filamentDensity}`,
    config.filamentDiameter == null ? null : `; filament_diameter = ${config.filamentDiameter}`,
    "; prusaslicer_config = end",
  ].filter(Boolean);

  if (options.fieldOrder === "config-first") {
    lines.push(...configLines, ...statsLines);
  } else {
    lines.push(...statsLines, ...configLines);
  }

  if (options.duplicate) {
    lines.push("; filament used [mm] = 3000.00");
  }

  if (options.extraTail) {
    lines.push(options.extraTail);
  }

  return `${lines.join(lineEnding)}${lineEnding}`;
}

async function parseFixture(content, options = {}) {
  const root = await createFixtureRoot();
  const filePath = await writeGcode(root, options.name || "fixture.gcode", content);
  const parsed = await parsePrusaSlicerGcode(filePath, {
    allowedRoots: [root],
    sliceParams: options.sliceParams,
    maxFileSizeBytes: options.maxFileSizeBytes,
  });
  return { root, filePath, parsed };
}

test("parses current Phase05-B synthetic cube expected metrics", async () => {
  const { parsed } = await parseFixture(completeGcode());

  assert.equal(parsed.parse.status, "parsed");
  assert.equal(parsed.result.print_time_seconds, 1496);
  assert.equal(parsed.result.silent_print_time_seconds, 1544);
  assert.equal(parsed.result.filament_length_microns, 2116640);
  assert.equal(parsed.result.filament_volume_mm3, 5090);
  assert.equal(parsed.result.filament_weight_mg, 6310);
  assert.equal(parsed.result.layer_count, 100);
  assert.equal(parsed.result.max_layer_z_microns, 20000);
  assert.deepEqual(parsed.result.dimensions, { x_mm: 20, y_mm: 20, z_mm: 20, source: "gcode_bounds" });
  assert.equal(parsed.result.filament_type, "PLA");
  assert.equal(parsed.result.printer_model, "Bambu Lab P1S");
  assert.equal(parsed.result.nozzle_diameter_microns, 400);
  assert.equal(parsed.result.layer_height_microns, 200);
  assert.equal(parsed.validation.metrics_status, "ok");
  assert.equal(parsed.validation.quote_ready, true);
  assert.deepEqual(parsed.validation.invalid_fields, []);
});

