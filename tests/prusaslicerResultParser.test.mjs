test("cloud geometry is used when G-code X/Y bounds are unavailable", async () => {
  const gcode = completeGcode().replace(/^G1 X.*$/gm, "");
  const root = await createFixtureRoot();
  const filePath = await writeGcode(root, "cloud-geometry.gcode", gcode);
  const parsed = await parsePrusaSlicerGcode(filePath, {
    allowedRoots: [root],
    input: { dimensions: { x_mm: 12, y_mm: 13, z_mm: 14 } },
  });

  assert.deepEqual(parsed.result.dimensions, { x_mm: 12, y_mm: 13, z_mm: 14, source: "cloud_file_geometry" });
});

test("cloud geometry fallback is explicit when no G-code bounds are available", async () => {
  const gcode = completeGcode({ layers: 0 }).replace(/^G1 X.*$/gm, "").replace(/^G1 Z.*$/gm, "").replace(/^;Z:.*$/gm, "");
  const root = await createFixtureRoot();
  const filePath = await writeGcode(root, "cloud-only.gcode", gcode);
  const parsed = await parsePrusaSlicerGcode(filePath, {
    allowedRoots: [root],
    input: { dimensions: { x_mm: 12, y_mm: 13, z_mm: 14 } },
  });

  assert.deepEqual(parsed.result.dimensions, { x_mm: 12, y_mm: 13, z_mm: 14, source: "cloud_file_geometry" });
  assert.equal(parsed.metric_sources.dimensions_source, "cloud_file_geometry");
  assert.ok(parsed.validation.warning_details.some((warning) => warning.severity === "MANUAL_REVIEW_REQUIRED"));
});

