test("slice params order changes keep the same SHA", () => {
  const first = createSliceParamsFingerprint({
    material: "PLA",
    printer_model: "Bambu Lab P1S",
    nozzle_diameter_microns: 400,
    layer_height_microns: 200,
    fill_density_percent: 50,
    support_mode: "none",
    brim_width_microns: 0,
  });
  const second = createSliceParamsFingerprint({
    brim_width_microns: 0,
    support_mode: "none",
    fill_density_percent: 50,
    layer_height_microns: 200,
    nozzle_diameter_microns: 400,
    printer_model: "Bambu Lab P1S",
    material: "PLA",
  });

  assert.equal(first.slice_params_json, second.slice_params_json);
  assert.equal(first.slice_params_sha256, second.slice_params_sha256);
});

test("slice params value changes change the SHA", () => {
  const first = createSliceParamsFingerprint({ fill_density_percent: 50 });
  const second = createSliceParamsFingerprint({ fill_density_percent: 15 });

  assert.notEqual(first.slice_params_sha256, second.slice_params_sha256);
});

test("project-owned 20mm cube STL fixture is deterministic and closed", async () => {
  const fixture = await readFile(join(process.cwd(), "tests/fixtures/prusaslicer/20mm-cube.stl"), "utf8");
  const sha = createHash("sha256").update(fixture).digest("hex");

  assert.equal(sha, "f5c3087986cbf6aa9545894e75c8ed173a8ff44c6e7978de933a36c1b68355ab");
  assert.equal((fixture.match(/facet normal/g) || []).length, 12);
  assert.match(fixture, /^solid make3d_phase06_20mm_cube/);
  assert.match(fixture, /endsolid make3d_phase06_20mm_cube\s*$/);
});
